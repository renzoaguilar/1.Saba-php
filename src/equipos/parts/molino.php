       <p>Para garantizar la mejor calidad en la taza y la mayor flexibilidad posible, hemos creado Elective  que funciona con las máquinas La Cimbali más avanzadas. Su combinación natural y óptima es con la M100. Gracias al sistema Bluetooth integrado en el molinillo dosificador, también las máquinas LaCimbali M100 y M39TE funcionan con Elective para garantizar una moledura del café siempre perfecta, entre otras cosas gracias a la tecnología PGS.</p>
