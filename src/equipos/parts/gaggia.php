            <h3>GAGGIA BABILA</h3>
        </div>
        <P>Gaggia Babila es una máquina automática de café espresso que produce bebidas de café de especialidad con solo tocar un botón. Programe hasta 7 bebidas especiales a  un toque, que van desde espresso, café largo, capuchinos clásicos y lattes. Cada bebida se puede personalizar en fuerza, volumen, temperatura y sabor preferidos. Las bebidas lácteas se preparan instantáneamente con solo presionar un botón gracias a la jarra de espuma higiénica y las calderas dobles.</P>
        <P>La Gaggia Babila incorpora un depósito de agua de 1.5 litros, un tanque adicional para la leche, de medio litro de capacidad, y un depósito para los granos de café bastante grande, de 300 gramos. La bomba de presión es de 15 bares, y por encima de todo llama la atención la presencia de dos calderas de acero inoxidable. Gracias a ella podemos preparar de manera simultánea café o usar el vaporizador, sin esperas y sin que un flujo de agua dependa del otro.</P>
