            <h3>FAEMA E61</h3>
        </div>
       <p>Un homenaje al eclipse solar ocurrido en Italia el mismo año, inmediatamente se convierte en un icono de rendimiento y estilo, gracias a sus soluciones técnicas e innovadoras, su cuerpo de acero puro y trabajo con elegante diseño de vidrio retroiluminado. La E61 es una Máquina de café espresso semiautomática diseñada específicamente para celebrar los 70 años de Faema.</p>
       <p>E61 es la primera máquina que utiliza una bomba volumétrica para proporcionar al agua la presión ideal de 9 atmósferas y mantener la presión constante durante todo el proceso de extracción, a diferencia de las máquinas de palanca. Además, gracias al sistema de preinfusión, la extensión del tiempo de contacto entre el agua y el café molido antes de la extracción favorece una extracción óptima en lo que se refiere a las propiedades organolépticas del espresso.</p>
